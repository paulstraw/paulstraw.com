      <p>
        Ever since I was 10 or 11, I’ve dreamed about living in an RV. The idea
        of having all my equipment with me, ready to go anywhere I wanted at a
        moment’s notice, seemed too good to be true. Of course, at the time, it
        mostly <em>was</em> too good to be true. Solar panels were extremely
        expensive, batteries stored very little energy, and high-speed wireless
        internet access was practically still the stuff of science fiction.
      </p>
      <p>
        Over the next decade and a half, that all changed. The proliferation of
        smart devices meant huge leaps in both cellular networking speed and
        lithium battery availability. Solar power became much more affordable.
        YouTube tutorials became ubiquitous, and suddenly anyone could find
        dozens of guides demonstrating how to take advantage of all these
        technologies.
      </p>
      <figure>
        <img
          src="/img/sprinter/first-trip.jpg"
          title="Oh, how I hated those decals."
        />
        <figcaption>
          The van on our very first trip up to Portland, pre-decal removal.
        </figcaption>
      </figure>
      <p>
        By the time 2017 rolled around, I was fortunate enough to have a
        fully-remote job in software engineering. At that point, my wife and I
        had tossed around the idea of living in an RV for a couple years, and we
        finally decided this was the right time to give it a shot.
      </p>
      <p>
        After lots of online research and visiting an RV show for some hands-on,
        we were firmly settled on getting something based on the Mercedes
        Sprinter chassis (aka a FedEx van). We found a good deal on a used 2006
        Sprinter conversion (Leisure Travel FS210B) that had been a rental for
        the last decade, bought it, and immediately headed up to Portland with
        our two dogs in tow.
      </p>
      <p>On our way back home from that inaugural trip, the engine exploded.</p>
      <hr />
      <h2>Space</h2>
      <p>
        Despite what you may have seen on Instagram, #vanlife isn’t all glamour.
        At the end of the day, you are literally living in a van. The extended
        wheelbase Sprinter is just shy of 22 feet long.<sup class="footnote-ref"
          ><a href="#fn1" id="fnref1">[1]</a></sup
        >
        This is great, because it can fit comfortably in most parking spaces.
        Spaces in the United States tend to be around 20 feet deep, and the
        extended wheelbase lets you back in several feet over the edge of
        parking lots or other unusable areas.
      </p>
      <p>
        The size is great for parking, but also makes for a truly tiny home.
        Inside, the Sprinter is 91 square feet.<sup class="footnote-ref"
          ><a href="#fn2" id="fnref2">[2]</a></sup
        >
        Actual floorspace (including fixed seating areas and bed) is just over
        60 square feet.<sup class="footnote-ref"
          ><a href="#fn3" id="fnref3">[3]</a></sup
        >
        It took many years and many moves to get to this point. Outside the van,
        we also rent a 25 square foot<sup class="footnote-ref"
          ><a href="#fn4" id="fnref4">[4]</a></sup
        >
        storage unit, and I’ve stored a few things like vinyl records and
        instruments at my parents’ house. Even with all the reduction in
        “stuff”, we’ve still had to squeeze every inch of space out of the van.
      </p>
      <responsive-figure position="left">
        <img
          src="/img/sprinter/stop-motion.jpg"
          title="We didn't have a printer, so everything was hand-drawn!"
        />
        <figcaption>
