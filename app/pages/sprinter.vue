      <h2>Miscellany</h2>
      <responsive-figure position="right">
        <video
          playsinline
          controls
          src="https://paulstraw.s3.amazonaws.com/sprinter/fridge-latch.mp4"
          poster="/video/sprinter/fridge-latch.jpg"
        ></video>
        <figcaption>
          Building and testing the fridge latch.
