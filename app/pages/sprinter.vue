          <a href="https://www.youtube.com/watch?v=VfJl4B4B32Q"
            >stop motion video</a
          >
          on the folding desk I added in front.
        </figcaption>
      </responsive-figure>
      <p>
        Leisure Travel’s conversion originally had an electric sofa in back that
        folded down to become the top part of the bed. We kept that system for
        around a year after moving into the van full-time, but eventually
        removed the sofa and replaced it with a permanent mattress running
        sideways in the van. This has worked pretty well for us, but would
        probably be quite uncomfortable for anyone over ~5’6”.<sup
          class="footnote-ref"
          ><a href="#fn5" id="fnref5">[5]</a></sup
        >
      </p>
      <p>
        Changing the bed layout gave us a permanent and more comfortable
        sleeping surface, easier access to the back of the van, and more
        under-bed storage space. Some of the freed up space toward the front of
        the van was repurposed into a cabinet, which provides a work surface,
        permanent dog bed, and additional storage.
      </p>
      <p>
        In front, I added a folding desk to take advantage of the passenger
        captain’s chair. The desk was originally designed to sit on the rubber
        stopper for the passenger sliding door, but I later modified it to allow
        hanging from some hooks above the door. This allows the door to be
        freely opened and closed while the desk is in use.
      </p>

