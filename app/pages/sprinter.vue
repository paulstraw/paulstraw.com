      <h2>Internet Connectivity</h2>
      <p>
        Since I’m a full time remote software engineer, consistent internet
        access is critical for me. Our primary internet access since moving into
        the van has been through the Verizon network. This has worked incredibly
        well, and we’ve had reliable service in the vast majority of spots
        across the country.
      </p>
      <responsive-figure position="right">
        <img
          src="/img/sprinter/point-mugu.jpg"
          title="One of the first places we went after moving in, and one of my personal favorites. 🌊"
        />
        <figcaption>
          The Sprinter hits the beach at Point Mugu, CA, post-decal removal.
        </figcaption>
      </responsive-figure>
      <p>
        Originally, we had this service through a company called
        <a href="https://unlimitedville.com">Unlimitedville</a>. They provide
        unlimited access to the major US networks with no throttling, and we
        consistently used hundreds of gigabytes of data each month without any
        issues for around a year. The only downside is that their service is
        quite expensive; we paid $250/month. In late 2018, we switched to a
        Verizon unlimited prepaid plan that also offered unlimited data, but for
        $65/month. Unfortunately, this plan was grandfathered in 2019, and is no
        longer available to sign up for.
      </p>
      <p>
        When traveling in Canada, we tethered to my phone. We have Cricket
        service, which allows for normal use of your plan in Canada and Mexico.
        My current plan has 10 gigabytes of tethered transfer,<sup
          class="footnote-ref"
          ><a href="#fn15" id="fnref15">[15]</a></sup
        >
        which is enough to get work done for a good while.
      </p>
      <p>
        We started out using the
        <a
          href="https://www.verizonwireless.com/internet-devices/verizon-jetpack-4g-lte-mobile-hotspot-ac791l/"
          >Netgear AC791L</a
        >
        and
        <a
          href="https://www.verizonwireless.com/internet-devices/verizon-jetpack-mifi-8800l/"
          >MiFi 8800L</a
        >
        modem/routers for internet access.<sup class="footnote-ref"
          ><a href="#fn16" id="fnref16">[16]</a></sup
        >
        For the most part they worked okay, but both were extremely prone to
        overheating. It seems like they definitely aren’t designed for extended
        use, or even to remain plugged in for an extended period. Eventually I
        decided they were too big a potential fire hazard to keep using
        constantly, and replaced them with a
        <a
          href="http://mofinetwork.com/index.php?main_page=product_info&amp;products_id=189"
          >MoFi 4500</a
        >. It’s much more configurable, has ethernet ports,<sup
          class="footnote-ref"
          ><a href="#fn17" id="fnref17">[17]</a></sup
        >
        and hasn’t had any overheating issues.
      </p>
      <p>
        If you’re looking into options for cell data plans, or equipment like
        modems or boosters, I recommend checking out the
        <a href="https://www.rvmobileinternet.com/overview/#cellular"
          >Mobile Internet Resource Center</a
        >. They’ve got lots of great articles and reviews, and really keep on
        top of the industry. They also make an iOS and Android app called
        <a href="http://www.twostepsbeyond.com/apps/coverage/">Coverage?</a>
        that I used many times throughout the trip. It was surprisingly accurate
        for the most part.
      </p>
