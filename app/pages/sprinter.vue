      <h3>Energy Conservation</h3>
      <p>
        To avoid DC -&gt; AC conversion losses (something like ~10%), we run as
        much as possible directly off 12 volt power. To help with this, I’ve
        added five 12 volt “cigarette lighter” accessory sockets, for a total of
        six running off the “house” battery bank. Five are in the back of the
        van, and one is in the cab. USB-C, and especially USB-C PD, have been a
        huge help in keeping things DC. All of our electronics other than
        Charlene’s laptop can be charged with either USB-A (“normal” USB),
        USB-C, or proprietary 12 volt adapters.
      </p>
      <p>
        The only things that permanently run off AC power are our refrigerator,
        my wife’s laptop charger, our TV, and an Apple TV.<sup
          class="footnote-ref"
          ><a href="#fn11" id="fnref11">[11]</a></sup
        >
        Those are all hooked to a
        <a
          href="https://www.victronenergy.com/inverters/phoenix-inverter-vedirect-250va-800va"
          >Victron Phoenix 12/500</a
        >, which runs 24/7. The AC electrical panel is powered by a
        <a
          href="https://www.victronenergy.com/inverters-chargers/multiplus-12v-24v-48v-800va-3kva"
          >Victron MultiPlus 12/3000/120</a
        >, which is primarily used for shore power passthrough and battery
        charging. When we’re on the road, we only turn it on to run higher-power
        devices like hair dryers. Running the larger inverter constantly would
        raise our energy usage due to its higher zero-load wattage.<sup
          class="footnote-ref"
          ><a href="#fn12" id="fnref12">[12]</a></sup
        >
      </p>
      <responsive-figure position="left">
        <img
          src="/img/sprinter/charlene-and-dogs.jpg"
          title="From left: Papers, Butters (ours), Ollie, Zippy (sister's)"
        />
        <figcaption>
          Charlene sitting on the bed holding our two dogs and two of her
          sister's.
        </figcaption>
      </responsive-figure>
      <p>
        I highly recommend all of Victron’s equipment. In addition to the two
        inverters, we also use their
        <a
          href="https://www.victronenergy.com/solar-charge-controllers/smartsolar-100-30-100-50"
          >Victron SmartSolar MPPT 100/50</a
        >
        solar charge controller, and
        <a href="https://www.victronenergy.com/battery-monitors/bmv-700"
          >Victron BMV-702</a
        ><sup class="footnote-ref"><a href="#fn13" id="fnref13">[13]</a></sup>
        battery monitor. All of their products have been very reliable,
        well-documented, and even frequently updated (both device firmware and
        iOS app). When on the road, being able to see historical yield
        information has been very useful.
      </p>
      <p>
        By far the largest electricity consumer in the van is the refrigerator.
        I installed a
        <a
          href="https://www.lowes.com/pd/frigidaire-4-5-cu-ft-freestanding-mini-fridge-freezer-compartment-silver-mist/50160171"
          >Frigidaire residential mini-fridge</a
        >, which has an estimated yearly electricity use of 265kWh.<sup
          class="footnote-ref"
          ><a href="#fn14" id="fnref14">[14]</a></sup
        >
        I ran a few short tests with a
        <a href="http://www.p3international.com/products/p4400.html"
          >Kill A Watt</a
        >
        after installation, and that estimate seems fairly accurate, or possibly
        a bit on the high side.
      </p>
      <p>
        Aside from a couple periods of multi-week heavy clouds, we’ve been able
        to comfortably maintain a charge just from solar power from around
        spring through fall in the United States. This only applies when the van
        is in fully or mostly-open areas. Shady spots are great for keeping the
        temperature down, but wreck solar yields. As of this writing, we’ve been
        staying at a very shady spot in Portland for the last several months,
        and only yield ~250-350Wh on sunny days. Sunny days in open spots give
        much higher yields of &gt;1.5-2kWh.
      </p>
