      </responsive-figure>
      <p>
        There have been tons of miscellaneous projects, challenges, and learning
        experiences in improving and living in the Sprinter. I'll go over a few
        of them here, but there are lots that didn't make the cut. Just because
        it's on wheels doesn't mean you're free from basic "homeownery" tasks
        and maintenance, either (hello re-caulking a bathroom).
      </p>
      <p>
        The fridge has worked quite well for the most part, and seems to be
        holding up well after 18 months. Its temperature isn’t always as
        consistent as would be ideal. To keep it cool enough, it has to be set
        so low that we sometimes end up with frozen items in the refrigerator
        compartment. This fridge was designed for residential use, but has a
        built-in childproofing lock. I modified the lock to have a permanent
        rotating key attached, which makes it simple to close up the fridge
        before driving.
      </p>
      <p>
        Water storage in the van is quite limited. It has a 26 gallon fresh
        water, 21 gallon gray water, and 10 gallon black water tank. While
        traveling, we consistently needed to fill up on fresh water and dump
        gray about every three days. The black tank generally needed to be
        dumped around once per week. Going into owning the van, I wasn’t sure
        how I would feel about the dumping process, but it’s really not bad to
        deal with.
      </p>
      <p>
        When we got the van, the Dometic kitchen faucet was damaged, and would
        often spray water out of the side. The entire fixture is cheap plastic
        that according to other reviewers is very prone to breaking.
        Unfortunately, as with many parts made for RVs, it’s still wildly
        overpriced at around
        <a href="https://www.amazon.com/dp/B00P03K8W2">$125</a>. In addition,
        due to the way the faucet head is oriented, it pointed directly out of
        the sink basin in the “open” position. We just lived with these
        frustrations for quite a while, but I eventually realized that the
        existing head just had standard 1/2” threads, and built a simple PVC
        replacement that has solved all of our issues with the original fixture
        head.
      </p>
      <p>
        Keeping cool was a major challenge during our travels. Since we were
        often off-grid, we were rarely able to run the air conditioning unit
        that came with the van. We kept it for a while and used it only when
        plugged in, but then in winter 2018 removed it and replaced with a
        <a href="https://www.airxcel.com/maxxair/products/fans/maxxfan-deluxe"
          >Maxxair MaxFan 5100K</a
        >. This has worked out great so far, even during a few 95º F<sup
          class="footnote-ref"
          ><a href="#fn18" id="fnref18">[18]</a></sup
        >
        days - admittedly parked in the shade. We don’t plan to take the van on
        trips during intense heat again, as even with the AC it could get pretty
        miserable.
      </p>
