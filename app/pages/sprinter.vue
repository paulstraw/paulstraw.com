      <p>
        The other side of climate control is insulation. We purchased a roll of
        Reflectix early on, and cut it to make covers for the front windows. To
        mount the window covers, we sliced slots into the edge of the Reflectix,
        inserted neodymium disc magnets, and sewed them in place with standard
        black edging. These have held up and worked very well, and cost very
        little to make.
      </p>
      <p>
        The Reflectix panels take care of both insulation and privacy for the
        front windows. The back windows are all tinted, providing daytime
        privacy. For nighttime, we got roller shades from
        <a href="https://mcdinnovations.com/">MCD</a>. They’re made of a
        high-quality material that completely blocks any light from getting
        through, but need frequent adjustment to avoid becoming stuck or very
        slow.
      </p>
      <hr />
      <h2>Back to That Engine</h2>
      <p>
        On the way home from our first trip after buying the van, the engine
        threw a rod. It was the 4th of July, but after a couple hours (and two
        tow trucks), we were able to get towed into Red Bluff, the nearest town.
        Of course, everything was closed, but we were dropped off outside a
        local mechanic where we could spend the night.<sup class="footnote-ref"
          ><a href="#fn19" id="fnref19">[19]</a></sup
        >
        Thankfully, everything worked out in the end, and we were able to get
        the engine replaced to get the van back on the road (after quite a few
        weeks, we got a rental car to get home in the meantime).
      </p>
      <p>
        After lots of cleanup and prep work,<sup class="footnote-ref"
          ><a href="#fn20" id="fnref20">[20]</a></sup
        >
        we moved into the van full-time in late 2017. After traveling the United
        States and Canada, we spent some time in California before finally
        taking the van back to Portland in early 2019.<sup class="footnote-ref"
          ><a href="#fn21" id="fnref21">[21]</a></sup
        >
        We’ve been at an RV park here since, and are planning to stay until we
        get a house in the area sometime during 2020. I’ve always wanted to try
        living in a van, but am still a bit surprised it’s worked as well as it
        has for this long.
      </p>

