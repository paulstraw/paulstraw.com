      <h2>Electricity</h2>
      <p>
        One of my main goals during planning was to reduce resource usage as
        much as possible. The fewer resources needed to keep running the van,
        the more flexibility we’d have when deciding where to stay, and for how
        long. Of course, we didn’t have much control over water storage since
        the van had already been converted, but electricity was entirely up to
        me.
      </p>
      <responsive-figure position="right">
        <img
          src="/img/sprinter/installing-solar.jpg"
          title="With some help from my dad!"
        />
        <figcaption>
          Installing the solar panels.
        </figcaption>
      </responsive-figure>
      <h3>Solar &amp; Battery Bank</h3>
      <p>
        You can only fit so many watts of solar panels on the roof of a
        Sprinter. I was able to fit four 170-watt panels, for a total
        theoretical 680 watts maximum.<sup class="footnote-ref"
          ><a href="#fn6" id="fnref6">[6]</a></sup
        >
        That’s not a lot of daily generation to deal with,<sup
          class="footnote-ref"
          ><a href="#fn7" id="fnref7">[7]</a></sup
        >
        so reducing electricity usage wherever possible was critical if we
        wanted to avoid having to frequently plug in for charging.
      </p>
      <p>
        Of course, solar panels are only half of the story for off-grid setups.
        Just as important is where you store energy generated by those panels
        for use during dark nights and cloudy days. Our power storage is four 12
        volt, 100 amp hour LiFePO4 batteries,<sup class="footnote-ref"
          ><a href="#fn8" id="fnref8">[8]</a></sup
        >
        for a total of 400 amp hours. There is all sorts of advice around how to
        optimally charge and discharge lithium batteries, but I’ve gone with 70%
        usable for my calculations. This gives 280 usable amp hours per charge,
        or ~3.4 kilowatt hours.
      </p>
      <h3>Lighting</h3>
      <p>
        When calculating electricity use, it’s really amazing how quickly small
        inefficiencies add up. The original conversion included six halogen
        <a
          href="https://www.hannarv.com/P/6499/ManufacturersSelectNIDPrizmSurfaceMountSwitchedOverheadLightBrushedNickel"
          >can light fixtures</a
        >
        underneath the cabinets.<sup class="footnote-ref"
          ><a href="#fn9" id="fnref9">[9]</a></sup
        >
        Each halogen bulb used 10 watts, which means running those lights for
        six hours in the evening would use 360 watt hours, or over 10% of our
        battery bank’s usable capacity!
      </p>
      <responsive-figure position="left">
        <video
          playsinline
          controls
          src="https://paulstraw.s3.amazonaws.com/sprinter/overhead-light-upgrade.mp4"
          poster="/video/sprinter/overhead-light-upgrade.jpg"
        ></video>
        <figcaption>
          Installation footage from the overhead light fluorescent to LED
          upgrade.
        </figcaption>
      </responsive-figure>
      <p>
        The overhead lights were similarly wasteful. Each of the three
        fluorescent fixtures used ~27 watts, nearly three times more than each
        of the halogen bulbs. I replaced the overhead lights with LED bulbs,
        which dropped their energy usage about 50%, to 14 watts each.
      </p>
      <p>
        I’ve loved controlling lights remotely for about as long as I can
        remember. My childhood bedrooms often had strings taped to
        lightswitches, draped around makeshift furniture-pulleys so I could turn
        them on or off from bed. As I got older, this transitioned to an
        interest in home automation, and I jumped on the Phillips Hue bandwagon
        pretty early on. Customizing light colors and controlling them with my
        phone was definitely something I wanted to maintain in the van.
      </p>
      <p>
        To scratch that itch, I decided to replace the under-cabinet lights with
        HomeKit-compatible LED ones. Adafruit’s 12-LED
        <a href="https://www.adafruit.com/product/2852">RGBW NeoPixel Ring</a>
        happened to be fit perfectly inside the existing fixtures, so I just
        removed the old bulbs. To tie everything together, I used a Raspberry Pi
        runing <a href="https://github.com/KhaosT/HAP-NodeJS">HAP-NodeJS</a> to
        integrate with HomeKit, and a simple Python script to actually
        communicate with the lights.<sup class="footnote-ref"
          ><a href="#fn10" id="fnref10">[10]</a></sup
        >
        The new lights have full color and brightness control, and only use
        about one watt each when on full brightness (usually a lot less, our
        standard evening setup averages around 2-3 watts), so it’s a huge energy
        savings with lots of additional functionality.
      </p>
      <figure>
        <video
          playsinline
          controls
          src="https://paulstraw.s3.amazonaws.com/sprinter/homekit-lights.mp4"
          poster="/video/sprinter/homekit-lights.jpg"
        ></video>
        <figcaption>
          The first demo of the replacement under-cabinet lights set up and
          talking to HomeKit. 🌈
