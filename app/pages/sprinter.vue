      <p>
        So, what did I learn from this admittedly somewhat ridiculous venture?
        First of all, I really like the much smaller than average space. No
        matter what size my living space is going forward, I want to apply the
        same drastic minimalism to my thinking as <em>has</em> to be applied to
        live in the van. That being said, I’m very much looking forward to a a
        normal desk/workspace, and a full-size shower.
      </p>
      <p>
        As I’ve already talked about in detail above, I really like being more
        conscious of my resource usage. I think a lot more about my water and
        electricity use, and garbage production than ever before. When your use
        of a resource has such a direct impact on your daily life, it really
        helps to put the importance of conservation into perspective.
      </p>
      <p>
        Even with all the rough patches and frustrations, I’m still very happy
        to have tried this. I learned tons about several different disciplines,
        saw huge chunks of North America that I’d never even been near before,
        and now kid me finally knows what it’s like to live in a van.
      </p>
